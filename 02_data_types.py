# strings
print("Hello World")
print('Hello World')
print('''Hello World''')
print("""Hello World""")
print(type("Hello World"))
# Concatenate strings
print("Bye" + "World")

# Numbers
# int
print(30)
print(type(30))
# float
print(30.1)
print(type(30.1))

# Boolean (First letter UPCASE)
print(False)
print(True)
print(type(False))

# List
print([10, 20, 30, 55])
print(["a", "b", "c"])
print([10, 20, 30, "hello"])
print(type([10, 20, 30, 55]))

# Tuples (its like a list that cannot change INMUTABLE) 
print(10,20, 30)
print(type((10,20, 30)))

# Dictionaries (group of key value)
print(
    {"name" : "Ryan",
    "lastname" : "Ray",
    "nickname" : "Fazt"}
)
print(type({"name" : "Ryan",
    "lastname" : "Ray",
    "nickname" : "Fazt"}))

# None
print(None)
print(type(None))

